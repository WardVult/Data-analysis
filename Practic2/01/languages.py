languages = ['Ukrainian', 'French', 'Bulgarian', 'Norwegian', 'Latvian', 'Spanish', 'German', 'Italian', 'Chinese', 'Japanese', 'Korean', 'Polish', 'Dutch', 'Portuguese']

languages_copy = languages.copy()

sorted_list = sorted(languages)

reversed_list = languages.copy()
reversed_list.reverse()

sorted_in_place = languages.copy()
sorted_in_place.sort()

print(languages_copy)
print(sorted_list)
print(reversed_list)
print(sorted_in_place)